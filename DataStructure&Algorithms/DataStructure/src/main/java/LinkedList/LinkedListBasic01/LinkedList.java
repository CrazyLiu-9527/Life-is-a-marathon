package LinkedList.LinkedListBasic01;

/**
 * 链表的基本结构
 *
 * @author liuzy
 * @date 2020/6/15 22:59
 */
public class LinkedList<E> {

    private class Node {
        public E e;
        public Node next;

        public Node(E e, Node next) {
            this.e = e;
            this.next = next;
        }

        public Node(E e) {
            this(e, null);
        }

        public Node() {
            this(null, null);
        }

        @Override
        public String toString() {
            return e.toString();
        }
    }
}
